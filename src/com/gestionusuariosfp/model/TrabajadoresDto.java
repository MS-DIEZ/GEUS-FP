package com.gestionusuariosfp.model;

public class TrabajadoresDto {
	
	private String trabajadoresString;
	private int idTrabajador;
	private int idTarea;

	public String getTrabajadoresString() {
		return trabajadoresString;
	}

	public void setTrabajadoresString(String trabajadoresString) {
		this.trabajadoresString = trabajadoresString;
	}

	public int getIdTrabajador() {
		return idTrabajador;
	}

	public void setIdTrabajador(int idTrabajador) {
		this.idTrabajador = idTrabajador;
	}

	public int getIdTarea() {
		return idTarea;
	}

	public void setIdTarea(int idTarea) {
		this.idTarea = idTarea;
	}

}
